              child: Container(
                color: Colors.red,
                height: 100,
              ),
            ),
            Flexible(
              child: Container(
                color: Colors.green,
                height: 200,
              ),
            ),
            Flexible(
              child: Container(
                color: Colors.blue,
                height: 300,
              ),
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
        child: const Icon(Icons.add),
      ),
    );
  }
